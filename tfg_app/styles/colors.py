from enum import Enum as enum

class Color(enum):
    PRIMARY="#14A1F0"
    SECONDARY="#087EC4"
    BACKGROUND="#003297"
    CONTENT="#171F26"

class TextColor(enum):
    HEADER = "#F1F2F4"
    BODY = "#C3C7CB"
    FOOTER = "#A3ABB2"

class LegendColor(enum):
    LEGEND_1 = "#00FF7F"
    LEGEND_2 = "#D3D3D3"
    LEGEND_1_1= "#FFA500"
    LEGEND_1_2= "#00D0FF"